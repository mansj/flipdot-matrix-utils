binlist = []
for line in fiveBySevenCharBitmaps.splitlines():
	line = line.strip()
	x = line.split(',')
	if not x[0]:continue
	binlist.append(x[0])

def getBinaryFromChar(c):
	fivebinlist = []
	pos = charlist.index(c)
	if (pos > 0): pos = pos * 5
	fivebinlist.append(binlist[pos])
	fivebinlist.append(binlist[pos + 1])
	fivebinlist.append(binlist[pos + 2])
	fivebinlist.append(binlist[pos + 3])
	fivebinlist.append(binlist[pos + 4])
	return fivebinlist
